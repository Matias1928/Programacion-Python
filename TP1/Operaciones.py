#Calculadora

def Suma():
        num1=float(input("Ingrese Valor:"))
        num2=float(input("Ingrese Valor:"))
        resutado=num1+num2
        return print("SU RESULTADO ES:",resutado)

    
def Resta():
        num1=float(input("Ingrese Valor:"))
        num2=float(input("Ingrese Valor:"))
        resutado=num1-num2
        return print("SU RESULTADO ES:",resutado)
        

def Multiplicacion():
        num1=float(input("Ingrese Valor:"))
        num2=float(input("Ingrese Valor:"))
        resutado=num1*num2
        return print("SU RESULTADO ES:",resutado)
       

def Division():
        num1=float(input("Ingrese Valor:"))
        num2=float(input("Ingrese Valor:"))
        resutado=num1/num2
        return print("SU RESULTADO ES:",resutado)